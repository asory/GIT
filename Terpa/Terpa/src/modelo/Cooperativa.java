package modelo;

public class Cooperativa {

	private String nombre;
	private String rif;

	public Cooperativa() {
		super();
	}

	public Cooperativa(String nombre, String rif) {
		super();
		this.nombre = nombre;
		this.rif = rif;

	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setRif(String rif) {
		this.rif = rif;
	}

	public String getRif() {
		return rif;
	}

}

